decimal_number = 59

# Convert to binary
binary_representation = bin(decimal_number)
print(f"Binary: {binary_representation}")  # Output: Binary: 0b111011

# Convert to octal
octal_representation = oct(decimal_number)
print(f"Octal: {octal_representation}")    # Output: Octal: 0o73

# Convert to hexadecimal
hexadecimal_representation = hex(decimal_number)
print(f"Hexadecimal: {hexadecimal_representation}")  # Output: Hexadecimal: 0x3b
